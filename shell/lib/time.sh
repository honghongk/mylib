#!/bin/bash

######################
# 현재시간을 출력한다
# 초단위까지 공백없이
######################
function time_stamp()
{
    date +%Y%m%d%H%M%S
}

######################
# 오늘날짜를 출력한다
# 공백없이
######################
function time_today()
{
    date +%Y%m%d
}

######################
# 현재시간을 unix 포맷으로 출력한다
######################
function time_unix()
{
    date +%s
}
